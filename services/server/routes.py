def _pick(namespace, keys):
    return {key: namespace[key] for key in keys if key in namespace}


def register_server_routes(app, namespace):
    register_public_routes(app, _pick(namespace, PUBLIC_ROUTE_KEYS))
    register_chat_routes(app, _pick(namespace, CHAT_ROUTE_KEYS))
    register_user_routes(app, _pick(namespace, USER_ROUTE_KEYS))
    register_operation_routes(app, _pick(namespace, OPERATION_ROUTE_KEYS))
